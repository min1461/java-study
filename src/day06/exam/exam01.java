package day06.exam;

public class exam01 {
	public static void main(String[] args) {
		// switch~case
		int n = 3;// 입력받은값
		switch (n) {
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("2");
			break;
		case 3:
			System.out.println("3");
			break;
		default:
			System.out.println("0");
			break;
		}
		System.out.println("프로그램 종료");

	}

}
